package main

import "fmt"


func main () {
		// constant variable
		const str string = "sahed";

		// int
		var num int = 23;
	
		// int unsigned
		var num1 uint= 34;
	
	
		// float 
		var f float32 = 3.4
		var f1 float64 = 32.4
	
		// short hand to declared variable
	
		person := "sahed"
	
		
	
		// compiler will set the variable type
		var normal = 23;
	
		fmt.Println(str);
		fmt.Println(num)
		fmt.Println(num1)
		fmt.Println(f)
		fmt.Println(f1)
		fmt.Println(person)
		fmt.Println(normal)
}